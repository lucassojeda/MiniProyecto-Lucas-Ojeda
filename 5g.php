            <p>Las redes 5G funcionan mediante el uso de frecuencias de radio más altas que las generaciones anteriores, específicamente en el rango de ondas milimétricas, lo que permite una mayor capacidad y velocidad de transmisión de datos. Estas ondas se transmiten a través de una red de estaciones base más densas que las usadas en 4G, permitiendo que más dispositivos se conecten simultáneamente sin perder rendimiento.</p>
            <p>El 5G utiliza tecnologías como MIMO masivo (múltiples entradas y salidas) para enviar y recibir múltiples señales a la vez, mejorando la eficiencia y la cobertura. Además, su baja latencia permite una respuesta casi inmediata, lo que es crucial para aplicaciones como los coches autónomos o la realidad aumentada. Sin embargo, las ondas de alta frecuencia tienen un alcance limitado, por lo que requieren más antenas para cubrir áreas extensas, especialmente en zonas urbanas.</p>
